from aiogram.types import InlineKeyboardMarkup
from aiogram.utils.keyboard import InlineKeyboardBuilder

# –ü–†–ò–ú–ï–ß–ê–ù–ò–ï: –ó–¥–µ—Å—å –Ω–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –∏–º–ø–æ—Ä—Ç–∞ CATALOG. 
# CATALOG –±—É–¥–µ—Ç –ø–µ—Ä–µ–¥–∞–≤–∞—Ç—å—Å—è –≤ —Ñ—É–Ω–∫—Ü–∏–∏ –∫–∞–∫ –∞—Ä–≥—É–º–µ–Ω—Ç.

def get_main_menu(catalog_data: dict) -> InlineKeyboardMarkup:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é —Å –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º–∏, –∏—Å–ø–æ–ª—å–∑—É—è –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å.
    
    :param catalog_data: –°–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏–π (iPhone, Mac –∏ —Ç.–¥.)
    """
    builder = InlineKeyboardBuilder()
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –¥–ª—è –∫–∞–∂–¥–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    for key, value in catalog_data.items():
        # callback_data: cat_iphones, cat_macbooks, cat_ipads, cat_watches
        builder.button(text=value['label'], callback_data=f"cat_{key}")
    
    # –†–∞—Å–ø–æ–ª–∞–≥–∞–µ–º –ø–æ 2 –∫–Ω–æ–ø–∫–∏ –≤ —Ä—è–¥
    builder.adjust(2) 
    return builder.as_markup()

def get_models_keyboard(catalog_data: dict, category_key: str) -> InlineKeyboardMarkup:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –º–æ–¥–µ–ª—è–º–∏ –¥–ª—è –ù–ï-iPhone –∫–∞—Ç–µ–≥–æ—Ä–∏–π.
    –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –±–æ–ª—å—à–µ –Ω–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è iPhone.
    """
    builder = InlineKeyboardBuilder()
    
    # –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –º–æ–¥–µ–ª–µ–π –ø–æ –∫–ª—é—á—É
    try:
        models = catalog_data[category_key]['models']
    except KeyError:
        # –ï—Å–ª–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω–∞, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
        return get_main_menu(catalog_data)

    for model in models:
        # callback_data: item_–ú–æ–¥–µ–ª—å
        builder.button(text=model, callback_data=f"item_{model}")
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É "–ù–∞–∑–∞–¥"
    builder.button(text="üîô –ù–∞–∑–∞–¥ –∫ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º", callback_data="back_to_main")
    
    builder.adjust(1)
    return builder.as_markup()


def get_dynamic_keyboard(data: list[str], callback_prefix: str, back_callback: str) -> InlineKeyboardMarkup:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –∏–∑ —Å–ø–∏—Å–∫–∞ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π (–¥–ª—è –ø–æ—à–∞–≥–æ–≤–æ–≥–æ –≤—ã–±–æ—Ä–∞ iPhone).
    
    :param data: –°–ø–∏—Å–æ–∫ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π (–Ω–∞–ø—Ä., ['256 GB', '512 GB'])
    :param callback_prefix: –ü—Ä–µ—Ñ–∏–∫—Å –¥–ª—è callback_data (–Ω–∞–ø—Ä., 'val_')
    :param back_callback: callback –¥–ª—è –∫–Ω–æ–ø–∫–∏ "–ù–∞–∑–∞–¥"
    """
    builder = InlineKeyboardBuilder()
    
    for item in data:
        # –í–ê–ñ–ù–û: –ö–æ–¥–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –≤ callback_data
        encoded_item = item.replace(" ", "_").replace("/", "-") 
        builder.button(text=item, callback_data=f"{callback_prefix}{encoded_item}")
    
    builder.button(text="üîô –ù–∞–∑–∞–¥", callback_data=back_callback)
    builder.adjust(1)
    return builder.as_markup()
